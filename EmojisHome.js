function selectRandomEmojis(array = updatedEmojiArray) {
  // Check if the array has enough elements
  if (array.length < 7) {
    console.error('Not enough elements in the array.');
    return '';
  }

  // Initialize an array to store the chosen indices
  const chosenIndices = [];

  // Generate 7 random, non-consecutive indices
  while (chosenIndices.length < 7) {
    const randomIndex = Math.floor(Math.random() * array.length);

    // Check if the index is not already chosen and not consecutive to the previous indices
    if (
      !chosenIndices.includes(randomIndex) &&
      !chosenIndices.some((prevIndex) => Math.abs(randomIndex - prevIndex) === 1)
    ) {
      chosenIndices.push(randomIndex);
    }
  }

  // Sort the indices to concatenate in order
  chosenIndices.sort((a, b) => a - b);

  // Use Array.slice() to get the 7 elements at the chosen indices
  const concatenatedString = chosenIndices.map((index) => array[index]).join('');
  
  return concatenatedString;
}

